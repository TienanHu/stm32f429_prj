/*----------------------------------------------------------------------------
  Hard Fault Handler
 *----------------------------------------------------------------------------*/

/*----------------------------------------------------------------------------
  Default Handler for Exceptions / Interrupts
 *----------------------------------------------------------------------------*/
void Default_Handler(void)
{
  while(1);
}

